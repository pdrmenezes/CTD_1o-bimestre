// if ternario

// if ternario: só aceita 2 condições, não aceita else if
// variável = condição ? "primeira expressão" : "segunda expressão"
// variável = condição ? true (if) : false (else)

// se a condição é verdadeira, executa a primeira expressão, se for falsa, executa a segunda expressão
// é obrigatório que exista segunda expressão. Se não quiser que faça nada, inserimos string vazia ('')
let dia = "domingo";
let resultado = dia == "domingo" ? "vou à praia" : "fico em casa;"

console.log(resultado)

// if ternário: if horizontal (variável + pergunta + if + else)

var oMaior = 4 > 10 ? "4 é maior que 10" : "10 é maior";
console.log(oMaior);

